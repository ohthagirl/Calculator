const calculate = {
    "/" : (firstNumber, secondNumber) => firstNumber / secondNumber,
    "*" : (firstNumber, secondNumber) => firstNumber * secondNumber,
    "+" : (firstNumber, secondNumber) => firstNumber + secondNumber,
    "-" : (firstNumber, secondNumber) => firstNumber - secondNumber,
    "=" : (firstNumber, secondNumber) => secondNumber,
};

export default calculate;